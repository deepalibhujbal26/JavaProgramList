package Class7;

public class Hw5 {
    public static void main(String[] args) {
        String[] animals={"Tiger","Lion","Elephant","Deer","Zebra"};
        for (int i = 0; i <animals.length ; i++) {

            System.out.print(animals[i]+" ");
        }
        System.out.println();
        int j=0;
        while(j<animals.length){
            System.out.print(animals[j]+" ");
            j++;
        }




    }
}
