package class1;

public class E7DataTypes {

    public static void main(String[] args) {


        // float short decimal number
        // hold the value 2 decimal
         float price = 12.3F;

         // double to store long decimal number
        //  floating point till 14
         Double pi=3.14526455825414155;

        System.out.println(price);
        System.out.println(pi);


        // store string character

        char gender='m';
        System.out.println(gender);


        //boolean used to store either true or false binanry information
        boolean isRaining= false;

        boolean isHungry= true;
        boolean wife= true;
        boolean husband= false;

        String name="Deepali";
        System.out.println(name);


    }
}
