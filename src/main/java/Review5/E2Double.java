package Review5;

public class E2Double {
    public static void main(String[] args) {
        double[]price={1.77,5.667,5656.56,5.45};
        for (int i =0;i<price.length;i++){
            System.out.print(price[i]+" ");
        }
        System.out.println();
        for(double number :price){
            System.out.print(number+" ");
        }
    }
}
